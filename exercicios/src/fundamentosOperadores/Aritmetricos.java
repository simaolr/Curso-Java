package fundamentosOperadores;

public class Aritmetricos {
	public static void main(String[] args) {
		
		System.out.println(2 + 3);
		
		var x = 34.56;
		double y = 2.2;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		int a = 8;
		int b = 3;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a / (double) b);
		
		System.out.println(a % b);
		System.out.println(8 % 3);
		
		// Em java é possivel realizar diversas operações apenas com os simbolos e dentor da tela de print
		// podendo ser guardadas as operações em variaveis.
	}
}
